              <h5 class="text__block-title">¿Cómo funciona?</h5>
              <p class="text__block-desc">
              La independización de viviendas es como dividir una casa o un edificio grande en pedazos más pequeños, como si cortaras una pizza en rebanadas. Cada pedazo es una unidad de vivienda separada.
              <br><br>
              <b>División Legal:</b> Lo primero que se hace es ir a las oficinas de gobierno locales para obtener permisos especiales. Esto es como obtener un permiso para dividir la pizza en rebanadas. Sin estos permisos, no puedes hacerlo.
              <br><br>
              <b>Separación de Servicios:</b> Luego, se instalan cosas como medidores de electricidad y agua separados para cada unidad. Es como tener un plato y cubiertos para cada rebanada de pizza, de modo que cada unidad tenga sus propios servicios.
              <br><br>
              <b>Papeleo:</b> Se preparan documentos legales para establecer oficialmente cada unidad como una vivienda independiente. Esto es como escribir los nombres de las personas que comerán cada rebanada de pizza.
              <br><br>
              <b>Listo para Vivir:</b> Una vez que todo esté listo y aprobado, cada unidad se considera una vivienda independiente. Cada persona o familia que vive en una unidad tiene su propio espacio y, por lo general, pueden comprarla o alquilarla por separado, como si cada rebanada de pizza se sirviera individualmente.
              <br><br>
              Así que, en resumen, la independización de viviendas es como dividir una propiedad grande en pedazos más pequeños, cada uno con sus propios servicios y documentos legales, para que las personas puedan vivir de manera independiente en cada pedazo.
            
