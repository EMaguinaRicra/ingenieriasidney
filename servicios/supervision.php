              <h5 class="text__block-title">Flujo de trabajo</h5>
              <p class="text__block-desc">
                <ol>
                    <li><strong>Planificación:</strong> Antes de que comience la construcción, se establece un plan que incluye especificaciones técnicas, plazos y presupuesto.</li>
                    <li><strong>Inicio de la construcción:</strong> Una vez que se aprueba el plan, comienza la construcción y el supervisor de obras está presente en el sitio para asegurarse de que todo comience según lo previsto.</li>
                    <li><strong>Control de calidad:</strong> Se realizan inspecciones para garantizar que los materiales y la mano de obra cumplan con los estándares establecidos en el plan.</li>
                    <li><strong>Seguimiento y coordinación:</strong> El supervisor monitorea el progreso de la construcción, coordina a los equipos y se asegura de que se siga el cronograma.</li>
                    <li><strong>Seguridad:</strong> Se implementan medidas de seguridad y se supervisan para proteger a los trabajadores y cumplir con las regulaciones.</li>
                    <li><strong>Resolución de problemas:</strong> Si surgen problemas, el supervisor los identifica y busca soluciones para mantener el proyecto en marcha.</li>
                    <li><strong>Documentación:</strong> Se mantienen registros detallados de inspecciones y avances para documentar el progreso y la calidad de la obra.</li>
                    <li><strong>Cierre del proyecto:</strong> Una vez que se completa la construcción, el supervisor de obras verifica que todo esté conforme con los estándares y normativas antes de la entrega final.</li>
